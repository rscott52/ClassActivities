// What should we expect to print?

// Basic Math
console.log(1 + 2 * 3); // should print 7
console.log((1 + 2) * 3); //should print 9
console.log(40 / 5 - 2 ** 3); // should print 0
console.log(6 % 2); //should print 0

// Adding/Concatenating Strings
console.log("10" + "5"); //should print "105"
console.log("Hello " + "world"); //should print "hello world"

// Data Coercion
console.log("2" + 2); //should print "22"
console.log(2 + true); //should print 3
console.log(true + true + "true" + "false"); //should print "2truefalse"
console.log(1 + 1 + "2" + "3"); // should print "223"
